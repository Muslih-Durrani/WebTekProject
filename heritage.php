                <h3 class="mt-4">Historical Significance<br></h3>
                <p>The Theodosian Walls, built in the early 5th century under Emperor Theodosius II, were a monumental defensive system that protected Constantinople, the capital of the Byzantine Empire, for over a thousand years. Stretching approximately 6.5 kilometers from the Sea of Marmara to the Golden Horn, the walls featured a complex design of double walls, towers, and a moat—making them one of the most advanced fortification systems of late antiquity. They successfully repelled numerous sieges, including those by the Huns, Arabs, and Bulgars, and only fell in 1204 to the Fourth Crusade and again in 1453 to the Ottomans. Their resilience and architectural ingenuity made them a symbol of the strength and endurance of Byzantium, and they remain a remarkable example of military engineering and urban defense.
                <br><br></p>

                <h3 class="mt-4">Architectural Features</h3>
                <ul>
                    <li><b>Double Wall System:</b> The fortifications consisted of two parallel walls: a taller inner wall (about 12 meters high) and a lower outer wall (about 8 meters high), creating a layered defense that was extremely difficult for attackers to breach.<br><br></li>
                    <li><b>Moat with Parapet:</b> In front of the outer wall was a wide moat (up to 20 meters wide and 7 meters deep) with its own low wall (parapet), adding a third layer of protection and making siege equipment harder to use.<br><br></li>
                    <li><b>Watchtowers and Gates:</b> The inner wall featured 96 square or rectangular towers, placed roughly every 55–70 meters, used for surveillance and archery defense. The walls also included strategic gates for military and civilian use, like the Golden Gate, used for imperial ceremonies.<br><br></li>
                </ul>

