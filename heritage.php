            <h2>Conservation Efforts</h2>
            <p>
The Theodosian Walls have undergone several phases of restoration over the centuries, with the most recent and systematic efforts led by the Istanbul Metropolitan Municipality (İBB) beginning in the 2010s and continuing into the 2020s. These restorations are part of a larger heritage conservation initiative coordinated by İBB Heritage (İBB Miras) and involve collaboration with universities and archaeological experts. The work focuses on structural stabilization, reconstruction of collapsed sections, and the preservation of original Byzantine masonry techniques, particularly in heavily deteriorated areas like Belgradkapı, Mevlanakapı, and Edirnekapı. Restoration efforts are funded by municipal budgets with support from the Ministry of Culture and Tourism and occasionally international heritage organizations. The goal is to both protect the historical integrity of the walls and integrate them into urban life through parks, walking paths, and educational signage.

</p>

